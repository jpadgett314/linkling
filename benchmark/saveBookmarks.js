import { check } from 'k6';
import http from 'k6/http';
import exec from 'k6/execution';

export const API_BASE_URL = 'http://localhost:3000/api/v1';
export const ACCESS_TOKEN = 'TEST';

export const options = {
  stages: [
    { duration: '10s', target: 10 },
    { duration: '90s', target: 10 },
    { duration: '10s', target: 0 },
  ],
  thresholds: {
    // Expect 95% of requests to complete under 500ms
    http_req_duration: ['p(95)<500'],
    // Expect a failure rate of less than 1%
    http_req_failed: ['rate<0.01'],
  },
};

function generateBody(vuId, iteration) {
  const uniqueId = `${vuId}-${iteration}-${Date.now()}`;

  return JSON.stringify({
    name: `Benchmark Link ${uniqueId}`,
    url: `https://example.com/test-page/${uniqueId}`,
    type: "url",
    description: "Generated by k6 throughput benchmark.",
    tags: [
      {
        id: 1,
        name: "benchmark"
      }
    ],
    collection: {
      id: 1,
      name: "Unorganized"
    }
  });
}

export default function () {
  const url = `${API_BASE_URL}/links`;

  const body = generateBody(exec.vu.idInTest, exec.vu.iterationInScenario);

  const params = {
    headers: {
      'Content-Type': 'application/json',
      'Accept': 'application/json',
      'Authorization': `Bearer ${ACCESS_TOKEN}`,
    },
  };

  const res = http.post(url, body, params);

  check(res, {
    'status is 200 or 201': (r) => r.status === 200 || r.status === 201,
  });
}
